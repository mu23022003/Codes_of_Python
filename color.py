import turtle

# Create a turtle instance
my_turtle = turtle.Turtle()

# Set the turtle's speed and color
my_turtle.speed(2)
my_turtle.color("purple")

# Move turtle to start position
my_turtle.up()
my_turtle.goto(-200, 0)  # Adjust the starting position as needed
my_turtle.down()

# Draw the name with 3D-like effect
def draw_name_3d(name, size):
    for i in range(size):
        my_turtle.forward(1)
        my_turtle.right(1)

    for i in range(size):
        my_turtle.forward(1)
        my_turtle.left(1)

    for i in range(size):
        my_turtle.forward(1)
        my_turtle.right(1)

    for i in range(size):
        my_turtle.forward(1)
        my_turtle.left(1)

# Draw each letter of the name
def draw_letter(letter):
    if letter == "J":
        draw_name_3d("J", 120)
    elif letter == "y":
        draw_name_3d("y", 100)
    elif letter == "o":
        draw_name_3d("o", 120)
    elif letter == "t":
        draw_name_3d("t", 80)
    elif letter == "s":
        draw_name_3d("s", 100)
    elif letter == "n":
        draw_name_3d("n", 100)
    elif letter == "a":
        draw_name_3d("a", 120)

# Define the name to be drawn
name = "Jyotsnaa"

# Draw the name
for letter in name:
    draw_letter(letter)

# Hide the turtle
my_turtle.hideturtle()

# Keep the turtle window open
turtle.done()
